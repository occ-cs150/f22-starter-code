/**
   Swaps values of the parameters provided the first parameter
   is larger than the second.
   @param[in,out] a  first input value
   @param[in,out] b second input value
*/
void sort2(int& a, int& b);


/**
   Sorts three integer values into ascending order.
   @param[in,out] a first input value
   @param[in,out] b second input value
   @param[in,out] c third input value
*/
void sort3(int& a, int& b, int& c);

